    private static final int baseYear = 1901;

    /**
     * 计算公历年当月的中气，公历月从0起始！
     * @param y 年
     * @param m 月
     * @return 中气日期
     */
    public static int principleTerm(int y, int m) {
        m++;

        if (y < baseYear || y > 2100) {
            return 0;
        }

        int index = 0;
        int ry = y - baseYear + 1;

        while (ry >= principleTermYear[m - 1][index]) {
            index++;
        }

        int term = principleTermMap[m - 1][4 * index + ry % 4];
        if ((ry == 171) && (m == 3)) {
            term = 21;
        }

        if ((ry == 181) && (m == 5)) {
            term = 21;
        }

        return term;
    }

    /**
     * 计算公历年当月的节气，公历月从0起始！
     * @param y 年
     * @param m 月
     * @return 每月节气日期
     */
    public static int sectionalTerm(int y, int m) {
        m++;
        if (y < baseYear || y > 2100) {
            return 0;
        }

        int index = 0;
        int ry = y - baseYear + 1;

        while (ry >= sectionalTermYear[m - 1][index]) {
            index++;
        }

        int term = sectionalTermMap[m - 1][4 * index + ry % 4];
        if ((ry == 121) && (m == 4)) {
            term = 5;
        }

        if ((ry == 132) && (m == 4)) {
            term = 5;
        }

        if ((ry == 194) && (m == 6)) {
            term = 6;
        }

        return term;
    }

